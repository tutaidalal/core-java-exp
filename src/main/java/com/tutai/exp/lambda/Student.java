package com.tutai.exp.lambda;

public class Student {

	int roll;
	int marks;
	
	public Student(int roll, int marks) {
		super();
		this.roll = roll;
		this.marks = marks;
	}

	public int getRoll() {
		return roll;
	}

	public void setRoll(int roll) {
		this.roll = roll;
	}

	public int getMarks() {
		return marks;
	}

	public void setMarks(int marks) {
		this.marks = marks;
	}
	
	
	
	
}
